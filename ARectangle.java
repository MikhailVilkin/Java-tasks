package com.company;


public class ARectangle
{
    public static void main(String[] args)
    {
        final int n = 5, m = 8;

        for (int i = 0; i<n; i++) {
            for (int j = 0; j < m; j++) {
                System.out.print("A");
            }
            System.out.printf("\n");
        }
    }
}
